square = 25
high = square
low = 0
epsilon = 0.0001
guess = (high + low) / 2.0
numGuesses = 0
while((guess ** 2) != square):
    print('high {} low {} guess {}'.format(high, low, guess))
    if ((((high + low) / 2.0 ) ** 2) > square):
        high = guess
    elif ((((high + low) / 2.0 ) ** 2) < square):
        low = guess
    guess = (((high + low) / 2.0))
    numGuesses += 1
print('{} is apparently the square root of {}'.format(guess, square))
print('Finding the square root took {} guesses. All of Charles Truscott\'s own work'.format(numGuesses))
