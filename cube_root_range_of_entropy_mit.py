# Cube Root Bisection Search
# Interesting that the range a values of cube roots takes on increases the possibility of finding a value over squares, e.g. less range for more decimal entropy

cube = 2481
high = cube
low = 0
epsilon = 0.1
guess = (high + low) / 2.0
numGuesses = 0
while((guess ** 3) != cube):
    print('high {} low {} guess {}'.format(high, low, guess))
    if ((((high + low) / 2.0 ) ** 3) > cube):
        high = guess
    elif ((((high + low) / 2.0 ) ** 3) < cube):
        low = guess
    guess = (((high + low) / 2.0))
    numGuesses += 1
print('{} is apparently the cube root of {}'.format(guess, cube))
print('Finding the cube root took {} guesses. '.format(numGuesses))

